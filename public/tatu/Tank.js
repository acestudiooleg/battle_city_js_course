import { white, darkGray } from './colors.js';

/**
 * üéÆ –ö–ª–∞—Å Tank - –±–∞–∑–æ–≤–∏–π –∫–ª–∞—Å –¥–ª—è –≤—Å—ñ—Ö —Ç–∞–Ω–∫—ñ–≤
 * 
 * –í—ñ–¥–ø–æ–≤—ñ–¥–∞—î –∑–∞:
 * - –ë–∞–∑–æ–≤—É –ª–æ–≥—ñ–∫—É —Ç–∞–Ω–∫–∞ (—è–∫ –ø—Ä–∞–≤–∏–ª–∞ –≥—Ä–∏!)
 * - –ú–∞–ª—é–≤–∞–Ω–Ω—è —Ç–∞–Ω–∫–∞
 * - –§—ñ–∑–∏–∫—É —Ä—É—Ö—É (—è–∫ —Ä—É—Ö–∞—î—Ç—å—Å—è —Ç–∞–Ω—á–∏–∫!)
 */

export class Tank {
    constructor(options = {}, logger) {
        // –ü–æ–∑–∏—Ü—ñ—è —Ç–∞–Ω–∫–∞ –Ω–∞ –ø–æ–ª—ñ (—è–∫ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–∏ –Ω–∞ –∫–∞—Ä—Ç—ñ!)
        // –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–∞ X (–∑–∞ –∑–∞–º–æ–≤—á—É–≤–∞–Ω–Ω—è–º 0)
        this.x = options.x || 0;
        // –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–∞ Y (–∑–∞ –∑–∞–º–æ–≤—á—É–≤–∞–Ω–Ω—è–º 0)
        this.y = options.y || 0;
        
        // –†–æ–∑–º—ñ—Ä–∏ —Ç–∞–Ω–∫–∞
        // —à–∏—Ä–∏–Ω–∞ —Ç–∞–Ω–∫–∞ –≤ –ø—ñ–∫—Å–µ–ª—è—Ö
        this.width = options.size || 32;
        // –≤–∏—Å–æ—Ç–∞ —Ç–∞–Ω–∫–∞ –≤ –ø—ñ–∫—Å–µ–ª—è—Ö
        this.height = options.size || 32;
        
        // –í–ª–∞—Å—Ç–∏–≤–æ—Å—Ç—ñ —Ç–∞–Ω–∫–∞ (—â–æ –≤–º—ñ—î —Ç–∞–Ω—á–∏–∫)
        // –∫–æ–ª—ñ—Ä —Ç–∞–Ω–∫–∞ (–∑–∞ –∑–∞–º–æ–≤—á—É–≤–∞–Ω–Ω—è–º –±—ñ–ª–∏–π)
        this.color = options.color || white;
        // —à–≤–∏–¥–∫—ñ—Å—Ç—å —Ä—É—Ö—É —Ç–∞–Ω–∫–∞
        this.speed = options.speed || 1;
        // –Ω–∞–ø—Ä—è–º–æ–∫ –¥—É–ª–∞ —Ç–∞–Ω–∫–∞ (–∫—É–¥–∏ –¥–∏–≤–∏—Ç—å—Å—è –¥—É–ª–æ!)
        this.direction = options.direction || 'up';
        
        // –°—Ç–∞–Ω —Ç–∞–Ω–∫–∞
        // —á–∏ –∂–∏–≤–∏–π —Ç–∞–Ω–∫
        this.isAlive = true;
        // –∑–¥–æ—Ä–æ–≤'—è —Ç–∞–Ω–∫–∞ (–≤—ñ–¥ 0 –¥–æ 100, —è–∫ –∑–¥–æ—Ä–æ–≤'—è –ª—é–¥–∏–Ω–∏!)
        this.health = 100;
        
        // –ú–µ–∂—ñ —Ä—É—Ö—É (—Ä–æ–∑–º—ñ—Ä–∏ Canvas) - —Å–ø—ñ–ª—å–Ω–∞ –≤–ª–∞—Å—Ç–∏–≤—ñ—Å—Ç—å
        this.bounds = {
            minX: 0,
            minY: 0,
            maxX: 800,
            maxY: 600,
        };
        
        // –õ–æ–≥–≥–µ—Ä –¥–ª—è –∑–∞–ø–∏—Å—É –ø–æ–¥—ñ–π
        this.logger = logger;
    }
    
    /**
     * –û–Ω–æ–≤–ª–µ–Ω–Ω—è —Å—Ç–∞–Ω—É —Ç–∞–Ω–∫–∞
     * @param {number} deltaTime - –ß–∞—Å –∑ –æ—Å—Ç–∞–Ω–Ω—å–æ–≥–æ –æ–Ω–æ–≤–ª–µ–Ω–Ω—è
     */
    update(deltaTime) {
        // –ë–∞–∑–æ–≤–∞ –ª–æ–≥—ñ–∫–∞ –æ–Ω–æ–≤–ª–µ–Ω–Ω—è (–ø–æ–∫–∏ —â–æ –ø–æ—Ä–æ–∂–Ω—è)
        // –í –Ω–∞—Å—Ç—É–ø–Ω–∏—Ö —É—Ä–æ–∫–∞—Ö —Ç—É—Ç –±—É–¥–µ –ª–æ–≥—ñ–∫–∞ —Ä—É—Ö—É —Ç–∞ —Å—Ç—Ä—ñ–ª—å–±–∏
    }
    
    /**
     * –ú–∞–ª—é–≤–∞–Ω–Ω—è —Ç–∞–Ω–∫–∞
     * @param {CanvasRenderingContext2D} ctx - –ö–æ–Ω—Ç–µ–∫—Å—Ç –¥–ª—è –º–∞–ª—é–≤–∞–Ω–Ω—è
     */
    render(ctx) {
        // —è–∫—â–æ —Ç–∞–Ω–∫ –º–µ—Ä—Ç–≤–∏–π, –Ω–µ –º–∞–ª—é—î–º–æ –π–æ–≥–æ
        if (!this.isAlive) return;
        
        // –∑–±–µ—Ä—ñ–≥–∞—î–º–æ –ø–æ—Ç–æ—á–Ω–∏–π —Å—Ç–∞–Ω –∫–æ–Ω—Ç–µ–∫—Å—Ç—É
        ctx.save();
        
        // –ú–∞–ª—é—î–º–æ —Ç—ñ–ª–æ —Ç–∞–Ω–∫–∞
        // –≤—Å—Ç–∞–Ω–æ–≤–ª—é—î–º–æ –∫–æ–ª—ñ—Ä —Ç–∞–Ω–∫–∞
        ctx.fillStyle = this.color;
        // –º–∞–ª—é—î–º–æ –ø—Ä—è–º–æ–∫—É—Ç–Ω–∏–∫
        ctx.fillRect(this.x, this.y, this.width, this.height);
        
        // –ú–∞–ª—é—î–º–æ –¥—É–ª–æ —Ç–∞–Ω–∫–∞
        // –≤–∏–∫–ª–∏–∫–∞—î–º–æ —Ñ—É–Ω–∫—Ü—ñ—é –º–∞–ª—é–≤–∞–Ω–Ω—è –¥—É–ª–∞
        this.drawBarrel(ctx);
        
        // –í–∏–∫–ª–∏–∫–∞—î–º–æ –º–µ—Ç–æ–¥ –¥–ª—è –º–∞–ª—é–≤–∞–Ω–Ω—è —Å–ø–µ—Ü–∏—Ñ—ñ—á–Ω–∏—Ö –ø–æ–∑–Ω–∞—á–æ–∫
        this.drawTankMark(ctx);
        
        // –≤—ñ–¥–Ω–æ–≤–ª—é—î–º–æ —Å—Ç–∞–Ω –∫–æ–Ω—Ç–µ–∫—Å—Ç—É
        ctx.restore();
    }
    
    /**
     * –ú–∞–ª—é–≤–∞–Ω–Ω—è –¥—É–ª–∞ —Ç–∞–Ω–∫–∞
     * @param {CanvasRenderingContext2D} ctx - –ö–æ–Ω—Ç–µ–∫—Å—Ç –¥–ª—è –º–∞–ª—é–≤–∞–Ω–Ω—è
     */
    drawBarrel(ctx) {
        // –¥–æ–≤–∂–∏–Ω–∞ –¥—É–ª–∞ (60% –≤—ñ–¥ —à–∏—Ä–∏–Ω–∏ —Ç–∞–Ω–∫–∞ - —è–∫ –¥–æ–≤–≥–∞ —Ç—Ä—É–±–∞!)
        const barrelLength = this.width * 0.6;
        // —à–∏—Ä–∏–Ω–∞ –¥—É–ª–∞ (20% –≤—ñ–¥ —à–∏—Ä–∏–Ω–∏ —Ç–∞–Ω–∫–∞ - —è–∫ —Ç–æ–Ω–∫–∞ —Ç—Ä—É–±–∞!)
        const barrelWidth = this.width * 0.2;
        
        // —Ç–µ–º–Ω–æ-—Å–∏–Ω—ñ–π –∫–æ–ª—ñ—Ä –¥–ª—è –¥—É–ª–∞ (—è–∫ –∫–æ–ª—ñ—Ä –º–µ—Ç–∞–ª—É!)
        ctx.fillStyle = darkGray;
        
        // –ø–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ –Ω–∞–ø—Ä—è–º–æ–∫ –¥—É–ª–∞ (–∫—É–¥–∏ –¥–∏–≤–∏—Ç—å—Å—è —Ç—Ä—É–±–∞!)
        switch (this.direction) {
            // —è–∫—â–æ –¥—É–ª–æ –¥–∏–≤–∏—Ç—å—Å—è –≤–≥–æ—Ä—É (—è–∫ —Ç—Ä—É–±–∞ –¥–∏–≤–∏—Ç—å—Å—è –≤ –Ω–µ–±–æ!)
            case 'up':
                ctx.fillRect(
                    // —Ü–µ–Ω—Ç—Ä—É—î–º–æ –¥—É–ª–æ –ø–æ X (—Ä–æ–∑–º—ñ—â—É—î–º–æ –ø–æ —Ü–µ–Ω—Ç—Ä—É!)
                    this.x + this.width / 2 - barrelWidth / 2,
                    // —Ä–æ–∑–º—ñ—â—É—î–º–æ –¥—É–ª–æ –≤–∏—â–µ —Ç–∞–Ω–∫–∞ (–Ω–∞–¥ —Ç–∞–Ω–∫–æ–º!)
                    this.y - barrelLength,
                    // —à–∏—Ä–∏–Ω–∞ –¥—É–ª–∞
                    barrelWidth,
                    // –¥–æ–≤–∂–∏–Ω–∞ –¥—É–ª–∞
                    barrelLength
                );
                break;
            // —è–∫—â–æ –¥—É–ª–æ –¥–∏–≤–∏—Ç—å—Å—è –≤–Ω–∏–∑ (—è–∫ —Ç—Ä—É–±–∞ –¥–∏–≤–∏—Ç—å—Å—è –≤ –∑–µ–º–ª—é!)
            case 'down':
                ctx.fillRect(
                    // —Ü–µ–Ω—Ç—Ä—É—î–º–æ –¥—É–ª–æ –ø–æ X (—Ä–æ–∑–º—ñ—â—É—î–º–æ –ø–æ —Ü–µ–Ω—Ç—Ä—É!)
                    this.x + this.width / 2 - barrelWidth / 2,
                    // —Ä–æ–∑–º—ñ—â—É—î–º–æ –¥—É–ª–æ –Ω–∏–∂—á–µ —Ç–∞–Ω–∫–∞ (–ø—ñ–¥ —Ç–∞–Ω–∫–æ–º!)
                    this.y + this.height,
                    // —à–∏—Ä–∏–Ω–∞ –¥—É–ª–∞
                    barrelWidth,
                    // –¥–æ–≤–∂–∏–Ω–∞ –¥—É–ª–∞
                    barrelLength
                );
                break;
            // —è–∫—â–æ –¥—É–ª–æ –¥–∏–≤–∏—Ç—å—Å—è –≤–ª—ñ–≤–æ (—è–∫ —Ç—Ä—É–±–∞ –¥–∏–≤–∏—Ç—å—Å—è –≤–ª—ñ–≤–æ!)
            case 'left':
                ctx.fillRect(
                    // —Ä–æ–∑–º—ñ—â—É—î–º–æ –¥—É–ª–æ –ª—ñ–≤—ñ—à–µ —Ç–∞–Ω–∫–∞ (–∑–ª—ñ–≤–∞ –≤—ñ–¥ —Ç–∞–Ω–∫–æ–º!)
                    this.x - barrelLength,
                    // —Ü–µ–Ω—Ç—Ä—É—î–º–æ –¥—É–ª–æ –ø–æ Y (—Ä–æ–∑–º—ñ—â—É—î–º–æ –ø–æ —Ü–µ–Ω—Ç—Ä—É!)
                    this.y + this.height / 2 - barrelWidth / 2,
                    // –¥–æ–≤–∂–∏–Ω–∞ –¥—É–ª–∞
                    barrelLength,
                    // —à–∏—Ä–∏–Ω–∞ –¥—É–ª–∞
                    barrelWidth
                );
                break;
            // —è–∫—â–æ –¥—É–ª–æ –¥–∏–≤–∏—Ç—å—Å—è –≤–ø—Ä–∞–≤–æ (—è–∫ —Ç—Ä—É–±–∞ –¥–∏–≤–∏—Ç—å—Å—è –≤–ø—Ä–∞–≤–æ!)
            case 'right':
                ctx.fillRect(
                    // —Ä–æ–∑–º—ñ—â—É—î–º–æ –¥—É–ª–æ –ø—Ä–∞–≤—ñ—à–µ —Ç–∞–Ω–∫–∞ (—Å–ø—Ä–∞–≤–∞ –≤—ñ–¥ —Ç–∞–Ω–∫–∞!)
                    this.x + this.width,
                    // —Ü–µ–Ω—Ç—Ä—É—î–º–æ –¥—É–ª–æ –ø–æ Y (—Ä–æ–∑–º—ñ—â—É—î–º–æ –ø–æ —Ü–µ–Ω—Ç—Ä—É!)
                    this.y + this.height / 2 - barrelWidth / 2,
                    // –¥–æ–≤–∂–∏–Ω–∞ –¥—É–ª–∞
                    barrelLength,
                    // —à–∏—Ä–∏–Ω–∞ –¥—É–ª–∞
                    barrelWidth
                );
                break;
        }
    }
    
    /**
     * –ú–∞–ª—é–≤–∞–Ω–Ω—è —Å–ø–µ—Ü–∏—Ñ—ñ—á–Ω–æ—ó –ø–æ–∑–Ω–∞—á–∫–∏ —Ç–∞–Ω–∫–∞ (–ø–µ—Ä–µ–≤–∏–∑–Ω–∞—á–∞—î—Ç—å—Å—è –≤ –¥–æ—á—ñ—Ä–Ω—ñ—Ö –∫–ª–∞—Å–∞—Ö)
     * @param {CanvasRenderingContext2D} ctx - –ö–æ–Ω—Ç–µ–∫—Å—Ç –¥–ª—è –º–∞–ª—é–≤–∞–Ω–Ω—è
     */
    drawTankMark(ctx) {
        // –ë–∞–∑–æ–≤–∏–π –º–µ—Ç–æ–¥ - –ø–æ—Ä–æ–∂–Ω—ñ–π, –ø–µ—Ä–µ–≤–∏–∑–Ω–∞—á–∞—î—Ç—å—Å—è –≤ –¥–æ—á—ñ—Ä–Ω—ñ—Ö –∫–ª–∞—Å–∞—Ö
    }
    
    /**
     * –í—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω—è –º–µ–∂ —Ä—É—Ö—É
     * @param {Object} bounds - –ú–µ–∂—ñ —Ä—É—Ö—É
     */
    setBounds(bounds) {
        this.bounds = { ...this.bounds, ...bounds };
    }
    
    /**
     * –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ –º–µ–∂ —Ä—É—Ö—É
     * @param {number} newX - –ù–æ–≤–∞ X –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–∞
     * @param {number} newY - –ù–æ–≤–∞ Y –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–∞
     * @returns {boolean} - true —è–∫—â–æ –ø–æ–∑–∏—Ü—ñ—è –≤ –º–µ–∂–∞—Ö
     */
    checkBounds(newX, newY) {
        return (
            newX >= this.bounds.minX &&
            newX + this.width <= this.bounds.maxX &&
            newY >= this.bounds.minY &&
            newY + this.height <= this.bounds.maxY
        );
    }
    
    /**
     * –û—Ç—Ä–∏–º–∞–Ω–Ω—è –ø–æ–∑–∏—Ü—ñ—ó –¥–ª—è —Å—Ç—Ä—ñ–ª—å–±–∏
     * @returns {Object} - –ü–æ–∑–∏—Ü—ñ—è –∫—É–ª—ñ
     */
    getShootPosition() {
        const centerX = this.x + this.width / 2;
        const centerY = this.y + this.height / 2;

        // –†–æ–∑—Ä–∞—Ö–æ–≤—É—î–º–æ –ø–æ–∑–∏—Ü—ñ—é –∫—É–ª—ñ –∑–∞–ª–µ–∂–Ω–æ –≤—ñ–¥ –Ω–∞–ø—Ä—è–º–∫—É
        switch (this.direction) {
            case 'up':
                return { x: centerX - 2, y: this.y - 4 };
            case 'down':
                return { x: centerX - 2, y: this.y + this.height };
            case 'left':
                return { x: this.x - 4, y: centerY - 2 };
            case 'right':
                return { x: this.x + this.width, y: centerY - 2 };
            default:
                return { x: centerX - 2, y: this.y - 4 };
        }
    }
    
    /**
     * –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ —á–∏ —Ç–∞–Ω–∫ –∂–∏–≤–∏–π
     * @returns {boolean} - true —è–∫—â–æ —Ç–∞–Ω–∫ –∂–∏–≤–∏–π
     */
    isTankAlive() {
        // —Ç–∞–Ω–∫ –∂–∏–≤–∏–π —è–∫—â–æ isAlive=true —ñ –∑–¥–æ—Ä–æ–≤'—è > 0
        return this.isAlive && this.health > 0;
    }
    
    /**
     * –ó–Ω–∏—â–∏—Ç–∏ —Ç–∞–Ω–∫
     */
    kill() {
        // –ø–æ–∑–Ω–∞—á–∞—î–º–æ —Ç–∞–Ω–∫ —è–∫ –º–µ—Ä—Ç–≤–∏–π
        this.isAlive = false;
        // –≤—Å—Ç–∞–Ω–æ–≤–ª—é—î–º–æ –∑–¥–æ—Ä–æ–≤'—è –≤ 0
        this.health = 0;
    }
    
    /**
     * –í—ñ–¥—Ä–æ–¥–∏—Ç–∏ —Ç–∞–Ω–∫
     */
    respawn() {
        // –ø–æ–∑–Ω–∞—á–∞—î–º–æ —Ç–∞–Ω–∫ —è–∫ –∂–∏–≤–∏–π
        this.isAlive = true;
        // –≤—ñ–¥–Ω–æ–≤–ª—é—î–º–æ –ø–æ–≤–Ω–µ –∑–¥–æ—Ä–æ–≤'—è
        this.health = 100;
    }
}